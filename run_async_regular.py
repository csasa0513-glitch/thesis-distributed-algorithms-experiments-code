"""
Q2: asynchronous gossip algorithm on the four baseline communication
topologies of Koshal et al. (2016): cycle, wheel (= star), grid, and
complete. This script is intentionally restricted to the Koshal
baseline; the Watts-Strogatz sweep belongs to Sec. 6.3 of the thesis
and is generated by `run_async_sensitivity.py`.

For every cell we run `N_REPS` independent sample paths (fresh gossip
seed) under the diminishing stepsize rule of Koshal et al.:

    alpha_{k,i} = 9 / Gamma_i^{k}   (Koshal 2016 Sec. 6.2)

Outputs written to `results/`:

    async_regular_tables_5_8.csv   mean error and 90% CI width at
                                   k in {5e4, 1e5}, for every cell
                                   of Tables 5--8
    async_regular_table_9.csv      k_eps, rho_min/rho_max and
                                   sqrt(lambda_2), N = 20

Usage:
    python run_async_regular.py
"""
from __future__ import annotations

import numpy as np
import pandas as pd

import config as C
from games.nash_cournot import solve_NE
from graphs.topologies import REGULAR
from algorithms.async_gossip import run as run_async
from analysis.metrics import summary as struct_summary
from analysis.plots import convergence_plot, mean_ci


# --------------------------------------------------------------------------
# Experiment parameters. R = 50 sample paths, matching Koshal et al. (2016)
# and the updated thesis (Chapter 6). Adjust N_REPS downwards for a faster
# sanity run.
# --------------------------------------------------------------------------
N_REPS: int = 50
K_HORIZONS: tuple[int, int] = (50_000, 100_000)
EPS_TABLE9: float = 1e-3
MAX_EVENTS_TABLE9: int = 500_000
Z_90: float = 1.645

# Four-topology setup (Chapter 6): the four baseline graphs of Koshal
# et al. (2016) -- cycle, wheel (= star), grid, complete. The WS
# sensitivity sweep belongs to Sec. 6.3 and is handled by
# run_async_sensitivity.py.
TOPOLOGIES: tuple[str, ...] = ("cycle", "star", "grid", "complete")


# --------------------------------------------------------------------------
# Helpers
# --------------------------------------------------------------------------
def _make_graph(name: str, N_val: int, seed: int | None):
    return REGULAR[name](N_val)


def _error_at(events: np.ndarray, errors: np.ndarray, target_k: int) -> float:
    idx = int(np.searchsorted(events, target_k, side="right") - 1)
    idx = max(0, min(idx, len(errors) - 1))
    return float(errors[idx])


def _run_one(G, x_star, rng: np.random.Generator,
             max_events: int, eps: float = 0.0):
    return run_async(G, x_star,
                     max_events=max_events,
                     eps=eps,
                     rng=rng)


# --------------------------------------------------------------------------
# Tables 5-8 : mean error and 90% CI width at k = 5e4 and k = 1e5
# --------------------------------------------------------------------------
def experiment_tables_5_to_8() -> pd.DataFrame:
    rows = []
    max_events = max(K_HORIZONS) + 1
    # Rerun BOTH N = 20 and N = 50 to match the most recent code changes:
    # equality constraint, exact Euclidean projection, corrected sync step
    # order, and per-N redrawn D_INTERCEPT (config.resample).
    for N_new in (20, 50):
        C.resample(N_new)
        x_star = solve_NE()

        # Collect convergence curves (one per topology) for the plot.
        curves: dict[str, tuple] = {}

        for name in TOPOLOGIES:
            err_runs = []
            err_trajectories = []   # full trajectories for the convergence plot
            events_ref = None       # event indices for the plot (same for all reps)
            for r in range(N_REPS):
                seed = (C.SEED
                        + 100_000 * N_new
                        + 100 * hash(name) % 10_000
                        + 10 * r
                        + 1)
                rng = np.random.default_rng(seed)
                # Fresh graph per rep for random families, static for regular
                graph_seed = int(rng.integers(1 << 31))
                G, _W = _make_graph(name, C.N, graph_seed)
                out = _run_one(G, x_star, rng, max_events)
                err_runs.append([_error_at(out["events"], out["rel_err"], k)
                                 for k in K_HORIZONS])
                err_trajectories.append(out["rel_err"])
                if events_ref is None:
                    events_ref = out["events"]
                # Progress heartbeat: print every 5 reps
                if (r + 1) % 5 == 0 or (r + 1) == N_REPS:
                    print(f"    [N={N_new} {name:8s}] rep {r+1:>2d}/{N_REPS} done",
                          flush=True)
            err_runs = np.asarray(err_runs)
            mean = err_runs.mean(axis=0)
            sem  = err_runs.std(axis=0, ddof=1) / np.sqrt(N_REPS)
            ci_w = 2.0 * Z_90 * sem
            for idx, k_val in enumerate(K_HORIZONS):
                rows.append({
                    "N":          N_new,
                    "topology":   name,
                    "k":          k_val,
                    "mean_error": float(mean[idx]),
                    "ci90_width": float(ci_w[idx]),
                })
            # Convergence curve: mean and 90% CI band over the R reps.
            mean_traj, low_traj, high_traj = mean_ci(
                list(err_trajectories), confidence=0.90)
            # Align with the event index (events_ref); fallback to indices.
            x_axis = (events_ref if events_ref is not None
                      else np.arange(len(mean_traj)))
            curves[name] = (x_axis, mean_traj, low_traj, high_traj)
            print(f"[tables 5-6] N={N_new:3d}  {name:8s}  "
                  f"e@5e4={mean[0]:.3e}  e@1e5={mean[1]:.3e}",
                  flush=True)
            # --- intermediate save: per topology, so we keep what we have if interrupted
            pd.DataFrame(rows).to_csv(
                C.RESULTS / "async_regular_tables_5_8.csv", index=False)

        # Write the convergence plot for this N (mirrors run_sync.py output).
        if curves:
            convergence_plot(
                curves,
                xlabel="gossip event k",
                ylabel="mean relative error e_k",
                title=f"Async algorithm, N={N_new} (mean over R={N_REPS})",
                out=C.RESULTS / f"async_convergence_N{N_new}.png",
            )
            print(f"[async] N={N_new}  convergence plot saved", flush=True)
    return pd.DataFrame(rows)


# --------------------------------------------------------------------------
# Table 9 : iterations to reach err < 1e-3 (N = 20, diminishing step)
# --------------------------------------------------------------------------
def experiment_table_9() -> pd.DataFrame:
    # Koshal Table 9 reports N = 20 only.
    C.resample(20)
    x_star = solve_NE()
    rows = []
    for name in TOPOLOGIES:
        k_eps_runs, struct_runs = [], []
        for r in range(N_REPS):
            seed = C.SEED + 9_000 + 17 * r + hash(name) % 1_000
            rng = np.random.default_rng(seed)
            graph_seed = int(rng.integers(1 << 31))
            G, W = _make_graph(name, C.N, graph_seed)
            struct_runs.append(struct_summary(G, W))
            out = _run_one(G, x_star, rng,
                           MAX_EVENTS_TABLE9, eps=EPS_TABLE9)
            k_eps_runs.append(out["k_eps"] if out["k_eps"] is not None
                              else MAX_EVENTS_TABLE9)
        # average structural indicators across the R graph samples
        keys = struct_runs[0].keys()
        struct_avg = {k: float(np.mean([s[k] for s in struct_runs])) for k in keys}
        rows.append({
            "topology":   name,
            "k_eps_mean": float(np.mean(k_eps_runs)),
            "k_eps_std":  float(np.std(k_eps_runs, ddof=1)),
            **struct_avg,
        })
        sqrt_lam2 = float(np.sqrt(max(0.0, 1.0 - struct_avg["spectral_gap"])))
        print(f"[table 9]  {name:8s}  "
              f"mean k_eps={np.mean(k_eps_runs):.2e}  "
              f"sqrt(lambda_2)={sqrt_lam2:.3f}")
    return pd.DataFrame(rows)


