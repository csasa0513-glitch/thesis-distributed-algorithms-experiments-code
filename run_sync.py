"""
Q1: synchronous algorithm on the four regular graphs (cycle, star,
grid, complete) reproducing Koshal et al. (2016) Tables 3-4 (static
network) at N in {20, 50}. This script is intentionally restricted to
the Koshal baseline; the Watts-Strogatz sweep belongs to Sec. 6.3 of
the thesis and is generated by run_sync_sensitivity.py.

The experiment has two parts.

* Distributed on regular graphs (Tables 3-4 analogue, top block):
  For every (N, topology in {cycle, star, grid, complete}) we run R_SYNC
  independent sample paths with a random initial decision
  x^0 ~ U[0, INIT_SCALE]^{2S}, projected onto X_i. Each player
  estimates the aggregate by consensus on v_i. The weight matrix W
  is fixed throughout all R_SYNC runs (deterministic regular graph).

* Centralized baseline (Tables 3-4 analogue, small-table form):
  The update x_i^{k+1} = Pi_{X_i}(x_i^k - tau_k F_i(x_i^k, bar s^k))
  does not reference W, so the iterate is independent of the topology.
  We therefore run the centralized algorithm only once per N
  (the same R_SYNC initial conditions as the distributed runs).

At each checkpoint k in SYNC_CHECKPOINTS (5000 and 10000, as in Koshal
Tables 1-4) we report the mean of the sample error

    e_k = max_{i,s} { |g_is^k - g_is*|, |s_is^k - s_is*| }
        / max_{i,s} { |g_is*|,         |s_is*|         }

(Koshal eq. (65)) and the width of the 90% CI of the mean,

    w_k = 2 * z_{0.95} * s_k / sqrt(R_SYNC),     z_{0.95} = 1.6449.

The stepsize is fixed at alpha_{k,i} = 1/k (Koshal Sec. 6.2).

Output:
    results/sync_regular.csv          long-format table; distributed
                                      rows have a topology entry, the
                                      centralized rows have topology = "--".
    results/sync_convergence_N20.png  distributed mean-error curves, N=20
    results/sync_convergence_N50.png  distributed mean-error curves, N=50

Usage:
    python run_sync.py
"""
from __future__ import annotations

import numpy as np
import pandas as pd

import config as C
from games.nash_cournot import solve_NE, project_Xi
from graphs.topologies import REGULAR
from algorithms.sync_koshal import run as run_sync
from analysis.plots import convergence_plot, mean_ci


# --------------------------------------------------------------------------
# Four-topology baseline (Chapter 6 of the thesis). N in {20, 50}.
# Four regular graphs (cycle, star, grid, complete) reproducing Koshal
# Tables 3 and 4 (static-network sync). The Watts-Strogatz sweep is in
# run_sync_sensitivity.py and the real-world case study is in
# run_real_network.py. The centralized baseline does not use W, so it
# is reported with only an (N, k) axis.
# --------------------------------------------------------------------------
N_SIZES: tuple[int, ...] = (20, 50)
CENTRALIZED_LABEL: str = "--"    # placeholder in the topology column

Z_90: float = 1.6449   # z_{0.95} for the two-sided 90% CI of the mean


def _solve_NE_for(N_new: int) -> np.ndarray:
    """Resample Cournot coefficients for N = N_new and solve the NE."""
    C.resample(N_new)
    return solve_NE()


def _random_x0(N: int, L: int, rng: np.random.Generator) -> np.ndarray:
    """Random initial decision, projected onto each player's X_i."""
    raw = rng.uniform(0.0, C.INIT_SCALE, size=(N, 2 * L))
    for i in range(N):
        raw[i] = project_Xi(i, raw[i])
    return raw


def _ci_width(values: np.ndarray) -> float:
    """Two-sided 90% CI width of the mean, normal approximation."""
    n = values.size
    if n < 2:
        return 0.0
    s = float(values.std(ddof=1))
    return 2.0 * Z_90 * s / np.sqrt(n)


def _run_batch(W: np.ndarray,
               x_star: np.ndarray,
               x0_batch: list[np.ndarray],
               known_aggregate: bool,
               label: str = "") -> np.ndarray:
    """
    Run the synchronous algorithm once per initial condition in `x0_batch`
    with a FIXED weight matrix `W`. Used for regular graphs and for the
    centralized baseline.
    Returns an (R, MAX_ITER_SYNC) array of sample errors.
    """
    R = len(x0_batch)
    errs = np.empty((R, C.MAX_ITER_SYNC))
    for r, x0 in enumerate(x0_batch):
        out = run_sync(
            W, x_star,
            max_iter=C.MAX_ITER_SYNC,
            eps=0.0,                 # never stop early
            record_every=1,
            x0=x0,
            known_aggregate=known_aggregate,
        )
        errs[r] = out["rel_err"]
        # live progress ping every 10 runs so the user can see the script is alive
        if (r + 1) % 10 == 0 or (r + 1) == R:
            print(f"    [{label}] run {r + 1}/{R} done", flush=True)
    return errs


def _checkpoint_rows(errs: np.ndarray,
                     N_new: int,
                     topology: str,
                     mode: str) -> list[dict]:
    """Extract mean and 90% CI at each checkpoint as long-format rows."""
    rows = []
    for k in C.SYNC_CHECKPOINTS:
        col = errs[:, k - 1]                  # 0-indexed: k updates -> index k-1
        rows.append({
            "N":         N_new,
            "topology":  topology,
            "mode":      mode,
            "k":         k,
            "mean_err":  float(col.mean()),
            "ci_width":  _ci_width(col),
            "n_runs":    int(errs.shape[0]),
        })
    return rows


def experiment_regular() -> pd.DataFrame:
    """
    Distributed runs on the four regular graphs plus a single centralized
    baseline per N. Records mean error and 90% CI width at every checkpoint.
    """
    rows: list[dict] = []

    for N_new in N_SIZES:
        print(f"\n=== N = {N_new} : solving NE and drawing initial conditions ===",
              flush=True)
        x_star = _solve_NE_for(N_new)
        N, L = C.N, C.L

        # Shared initial conditions across distributed and centralized runs,
        # so that within a given (N, k) row the centralized baseline is
        # directly comparable to every distributed topology entry.
        rng = np.random.default_rng(C.SEED + 1000 + N_new)
        x0_batch = [_random_x0(N, L, rng) for _ in range(C.R_SYNC)]

        # ---------- centralized baseline: run once, topology-free --------
        # The centralized update ignores W, so any W works. We pass the
        # complete-graph W as a harmless placeholder.
        print(f"--- N={N_new}  centralized baseline  ({C.R_SYNC} runs x {C.MAX_ITER_SYNC} iter) ---",
              flush=True)
        _, W_placeholder = REGULAR["complete"](N)
        errs_cent = _run_batch(W_placeholder, x_star, x0_batch,
                               known_aggregate=True,
                               label=f"N={N_new} cent")
        rows.extend(_checkpoint_rows(errs_cent, N_new,
                                     CENTRALIZED_LABEL, "centralized"))
        print(f"[sync] N={N_new:2d}  centralized       "
              f"k=5e3 mean={errs_cent[:, 4999].mean():.3e}  "
              f"k=1e4 mean={errs_cent[:, 9999].mean():.3e}")
        # --- intermediate save: if the script crashes later we keep what we have
        pd.DataFrame(rows).to_csv(C.RESULTS / "sync_regular.csv", index=False)
        print(f"[sync] partial CSV saved ({len(rows)} rows so far)",
              flush=True)

        # ---------- distributed: one batch per regular topology ----------
        stride = 50
        curves: dict[str, tuple] = {}

        for name, gen in REGULAR.items():
            print(f"--- N={N_new}  distributed  topology={name}  "
                  f"({C.R_SYNC} runs x {C.MAX_ITER_SYNC} iter) ---",
                  flush=True)
            _, W = gen(N)
            errs_dist = _run_batch(W, x_star, x0_batch,
                                   known_aggregate=False,
                                   label=f"N={N_new} {name}")
            rows.extend(_checkpoint_rows(errs_dist, N_new, name, "distributed"))
            # --- intermediate save per topology
            pd.DataFrame(rows).to_csv(C.RESULTS / "sync_regular.csv", index=False)

            # Build (iters, mean, low, high) with proper 90% CI band.
            mean_traj, low_traj, high_traj = mean_ci(list(errs_dist), confidence=0.90)
            iters = np.arange(1, C.MAX_ITER_SYNC + 1)
            sel = iters[::stride]
            curves[name] = (sel,
                            mean_traj[::stride],
                            low_traj[::stride],
                            high_traj[::stride])

            print(f"[sync] N={N_new:2d}  dist {name:9s}  "
                  f"k=5e3 mean={errs_dist[:, 4999].mean():.3e}  "
                  f"k=1e4 mean={errs_dist[:, 9999].mean():.3e}")

        convergence_plot(
            curves,
            xlabel="iteration k",
            ylabel="mean relative error e_k",
            title=f"Sync algorithm, N={N_new} (distributed, mean over R={C.R_SYNC})",
            out=C.RESULTS / f"sync_convergence_N{N_new}.png",
        )
        print(f"[sync] N={N_new}  done, "
              f"{len(curves)} distributed topologies (4 regular) "
              f"+ 1 centralized baseline")

    return pd.DataFrame(rows)


def _pivot_dist(df: pd.DataFrame, field: str) -> pd.DataFrame:
    """Distributed pivot: (N, k) rows  x  topology columns."""
    sub = df[df["mode"] == "distributed"]
    piv = sub.pivot_table(index=["N", "k"], columns="topology", values=field)
    # Column order: 4 regular baseline graphs (cycle, star, grid, complete).
    ordered_cols = list(REGULAR.keys())
    return piv[[c for c in ordered_cols if c in piv.columns]]


def _pivot_cent(df: pd.DataFrame, field: str) -> pd.DataFrame:
    """Centralized pivot: N rows x k columns (topology-free)."""
    sub = df[df["mode"] == "centralized"]
    return sub.pivot_table(index="N", columns="k", values=field)


def main() -> None:
    regular_names = list(REGULAR.keys())
    print("=" * 70, flush=True)
    print(f"run_sync.py : sync algorithm reproduction of Koshal et al. 2016",
          flush=True)
    print(f"              on the four baseline graphs",
          flush=True)
    print(f"  N sizes           : {N_SIZES}", flush=True)
    print(f"  R (sample paths)  : {C.R_SYNC}", flush=True)
    print(f"  Max iterations    : {C.MAX_ITER_SYNC}", flush=True)
    print(f"  Checkpoints       : {C.SYNC_CHECKPOINTS}", flush=True)
    print(f"  Regular topologies: {regular_names}", flush=True)
    print("=" * 70, flush=True)

    df = experiment_regular()
    df.to_csv(C.RESULTS / "sync_regular.csv", index=False)

    print("\n==== distributed : mean error  (Table 1) ====")
    print(_pivot_dist(df, "mean_err"))
    print("\n==== distributed : 90% CI width  (Table 2) ====")
    print(_pivot_dist(df, "ci_width"))
    print("\n==== centralized : mean error  (Table 3, small form) ====")
    print(_pivot_cent(df, "mean_err"))
    print("\n==== centralized : 90% CI width  (Table 4, small form) ====")
    print(_pivot_cent(df, "ci_width"))


if __name__ == "__main__":
    main()
